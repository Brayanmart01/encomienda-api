     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $order = new orders();
        $order->userEmail = $request->userEmail;
        $order->package = $request->package;
        $order->direction = $request->direction;
        $order->rateDistance = $request->rateDistance;
        $order->timeDistance = $request->timeDistance;

        $order->save();
    }

